public class ArrayEx03{
    public static void main(String[] args){
        System.out.println("시작");
     
        //System.out.println(args.length);
        for(int i=0; i<args.length; i++){
            System.out.println(args[i]+"\t");
        }
        System.out.println();

        System.out.println("끝");
    }
}