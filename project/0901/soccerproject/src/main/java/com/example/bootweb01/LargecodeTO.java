package com.example.bootweb01;

public class LargecodeTO {
	private String seq;
	private String largecode;
	private String largeinfo;
	private String largeremark;
	
	

	public String getLargeremark() {
		return largeremark;
	}
	public void setLargeremark(String largeremark) {
		this.largeremark = largeremark;
	}
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getLargecode() {
		return largecode;
	}
	public void setLargecode(String largecode) {
		this.largecode = largecode;
	}
	public String getLargeinfo() {
		return largeinfo;
	}
	public void setLargeinfo(String largeinfo) {
		this.largeinfo = largeinfo;
	}
	
	
}
