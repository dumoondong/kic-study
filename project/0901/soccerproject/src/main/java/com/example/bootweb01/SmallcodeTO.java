package com.example.bootweb01;

public class SmallcodeTO {
	private String seq;
	private String smallcode;
	private String smallinfo;
	private String smallremark;
	
	

	public String getSmallremark() {
		return smallremark;
	}
	public void setSmallremark(String smallremark) {
		this.smallremark = smallremark;
	}
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getSmallcode() {
		return smallcode;
	}
	public void setSmallcode(String smallcode) {
		this.smallcode = smallcode;
	}
	public String getSmallinfo() {
		return smallinfo;
	}
	public void setSmallinfo(String smallinfo) {
		this.smallinfo = smallinfo;
	}
	
	
}
